package bean;

public class comp_bean{
    private String code, c_value, uname, id;

    public void setCode(String code){
            this.code = code;
    }
    public String getCode(){
            return code;
    }
    
    public void setCvalue(String cvalue){
            this.c_value = cvalue;
    }
    public String getCvalue(){
        return c_value;
    }
    
    public void setUname(String uname){
        this.uname = uname;
    }
    public String getUname(){
        return uname;
    }
    
    public void setId(String id){
        this.id = id;
    }
    public String getId(){
        return id;
    }
}